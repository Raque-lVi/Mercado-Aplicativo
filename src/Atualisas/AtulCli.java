    private void tbAtuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbAtuaActionPerformed
        Connection con = conexaoBanco.AbrirConexao();
        AcessoDAO ac = new AcessoDAO (con);
        PessoaDAO psd = new PessoaDAO();

        Pessoa p  = new Pessoa();
        endereco end = new endereco();

        p.setDatanasc(txdatadn.getText());
        p.setLogin(txlogin.getText());
        p.setNome(txnome.getText());
        p.setSenha(txsenha.getText());
        p.setTelefone(txTelefone.getText());
        
        p.setCpf(txcpf.getText());    
        end.setFkcpf(txcpf.getText());        
        
        end.setBairro(txbairro.getText());
        end.setCep(txcep.getText());
        end.setCidade(txcidade.getText());
        end.setEndereco(txendereco.getText());
        end.setNumerResiden(txnr.getText());
        end.setUr(txur.getText());
        
        Pessoa.add(p);
        endereco.add(end);
    
    
   psd.AtualEnd(end);
   psd.AtualPessoa(p);
  
        
        
    }//GEN-LAST:event_tbAtuaActionPerformed

