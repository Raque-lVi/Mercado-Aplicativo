    private void btatualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btatualizarActionPerformed
        Connection con = conexaoBanco.AbrirConexao();
        AcessoDAO ac = new AcessoDAO (con);
        Pessoa p = new Pessoa();
        funcionarioDAO fund = new funcionarioDAO();

        funcionario fc = new funcionario();
        endereco en = new endereco();
        PessoaDAO psd = new PessoaDAO();

        p.setCpf(txcpf.getText());
        p.setDatanasc(txdatadn.getText());
        p.setTelefone(txtc.getText());
        p.setNome(txnome.getText());


        en.setBairro(txbairro.getText());
        en.setCep(txcep.getText());
        en.setCidade(txcidade.getText());
        en.setCodigo(txCodigo.getText());
        en.setEndereco(txendereco.getText());       
        en.setFkcpf(txcpf.getText()); 
        en.setNumerResiden(txnr.getText());       
        en.setUr(txur.getText()); 

        fc.setFkcpf(txcpf.getText());
        fc.setMatricula(txMatricula.getText()); 
        fc.setSalario(Float.parseFloat(txsalario.getText()));     
        fc.setTurno(txturno.getText());

        Pessoa.add(p);
        endereco.add(en);
        funcionario.add(fc);

        psd.AtualEnd(en);
        psd.AtualPessoa(p);
        fund.AtualFuncio(fc);
        
    }//GEN-LAST:event_btatualizarActionPerformed

